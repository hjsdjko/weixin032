				kechengmingchengIndex: 0,
				// 登录用户信息
				user: {},
                ro:{
                   xuehao : false,
                   xingming : false,
                   banji : false,
                   kechengmingcheng : false,
                   chengji : false,
                   jiaoshigonghao : false,
                   jiaoshixingming : false,
                   faburiqi : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor,
